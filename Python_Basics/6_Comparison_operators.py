num1 = 0                    #Initializing Variables
num2 = 1

#Boolean operators always return Boolean values
print(num1 > num2)          #Greater than
print(num1 < num2)          #Less than
print(num1 >= num2)         #Greater than equal to
print(num1 <= num2)         #Less than equal to
print(num1 == num2)         #Equality
print(num1 != num2)         #not equal

#Logical Operators
print(num1 and num2)        #Logical AND
print(num1 or num2)         #Logical OR
print(not num1)             #NOT(Negation)